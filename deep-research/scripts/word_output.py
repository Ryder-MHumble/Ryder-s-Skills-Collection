def add_hr_line(doc):
    """细蓝色横线 - 章节分隔"""
    p = doc.add_paragraph()
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '6')
    bottom.set(qn('w:space'), '1')
    bottom.set(qn('w:color'), '1F4E79')
    pBdr.append(bottom)
    pPr.append(pBdr)
    return p


def add_section_cover(doc, chapter_num, chapter_title, subtitle=None):
    """麦肯锡风章节封面：横线 + 章节号 + 标题 + 副标题（可选）"""
    add_hr_line(doc)
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(12)
    run = p.add_run(f'第 {chapter_num} 章')
    run.font.size = Pt(11)
    run.font.color.rgb = COLOR_TERTIARY
    p2 = doc.add_paragraph()
    run = p2.add_run(chapter_title)
    run.font.size = Pt(22)
    run.font.bold = True
    run.font.color.rgb = COLOR_PRIMARY
    p2.paragraph_format.space_after = Pt(2)
    if subtitle:
        p3 = doc.add_paragraph()
        run = p3.add_run(subtitle)
        run.font.size = Pt(11)
        run.font.color.rgb = COLOR_SECONDARY
        run.font.italic = True
    add_hr_line(doc)
    doc.add_paragraph()


def add_dense_cover(doc, params):
    """
    v5.2 信息密集型首页（麦肯锡风 - 单页所有信息）

    设计目标：13-15cm 总高 < A4 可用 24.7cm，单页承载所有关键信息：
    - 顶部 brand 浅蓝条
    - 主标题 + 副标题 + 英文（紧凑）
    - 5 条核心判断（紧凑横排）
    - 关键数据 + 元信息（2 列表格）
    - Banner 图（缩小版 16cm 宽）
    - 底部 brand 浅灰条

    params dict 必填字段：
    - brand_top: str  (顶部 brand 条文字)
    - title: str  (主标题，第 1 行)
    - title_line2: str  (主标题，第 2 行 / 副标)
    - subtitle_en: str  (英文副标)
    - judgments: List[Tuple[label, desc]]  (5 条核心判断)
    - metrics: List[Tuple[big, desc]]  (4 个关键数据)
    - metas: List[Tuple[label, value]]  (6 项元信息)
    - footer: str  (底部 brand 条文字)
    """
    # 1. 顶部 brand 浅蓝条
    p_brand_top = doc.add_paragraph()
    p_brand_top.paragraph_format.space_before = Pt(0)
    p_brand_top.paragraph_format.space_after = Pt(0)
    pPr = p_brand_top._p.get_or_add_pPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:fill'), '1F4E79')
    pPr.append(shd)
    run = p_brand_top.add_run('  ' + params.get('brand_top', 'RYDER AI SIGNAL  ·  深度调研报告'))
    run.font.size = Pt(9)
    run.font.bold = True
    run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)

    # 2. 主标题区（紧凑）
    p_t1 = doc.add_paragraph()
    p_t1.alignment = WD_ALIGN_PARAGRAPH.LEFT
    p_t1.paragraph_format.space_before = Pt(8)
    p_t1.paragraph_format.space_after = Pt(0)
    run = p_t1.add_run(params['title'])
    run.font.size = Pt(28)
    run.font.bold = True
    run.font.color.rgb = COLOR_PRIMARY

    p_t2 = doc.add_paragraph()
    p_t2.paragraph_format.space_before = Pt(2)
    p_t2.paragraph_format.space_after = Pt(0)
    run = p_t2.add_run(params['title_line2'])
    run.font.size = Pt(14)
    run.font.bold = True
    run.font.color.rgb = COLOR_SECONDARY

    p_t3 = doc.add_paragraph()
    p_t3.paragraph_format.space_before = Pt(0)
    p_t3.paragraph_format.space_after = Pt(4)
    run = p_t3.add_run(params['subtitle_en'])
    run.font.size = Pt(9)
    run.font.italic = True
    run.font.color.rgb = COLOR_TERTIARY

    # 3. 核心判断（紧凑横排）
    p_jh = doc.add_paragraph()
    p_jh.paragraph_format.space_before = Pt(2)
    p_jh.paragraph_format.space_after = Pt(2)
    run = p_jh.add_run('核心判断  /  KEY JUDGMENTS')
    run.font.size = Pt(9)
    run.font.bold = True
    run.font.color.rgb = COLOR_PRIMARY

    for label, desc in params['judgments']:
        p_j = doc.add_paragraph()
        p_j.paragraph_format.space_before = Pt(0)
        p_j.paragraph_format.space_after = Pt(1)
        run = p_j.add_run(f'  {label}    ')
        run.font.size = Pt(9.5)
        run.font.bold = True
        run.font.color.rgb = COLOR_PRIMARY
        run = p_j.add_run(desc)
        run.font.size = Pt(9.5)
        run.font.color.rgb = COLOR_BODY

    # 4. 关键数据 + 元信息（2 列表格）
    table1 = doc.add_table(rows=1, cols=2)
    table1.alignment = WD_TABLE_ALIGNMENT.LEFT
    table1.autofit = False
    table1.columns[0].width = Cm(8.5)
    table1.columns[1].width = Cm(8.5)
    for cell in table1.rows[0].cells:
        cell.width = Cm(8.5)

    # 左列：关键数据
    left = table1.rows[0].cells[0]
    left._tc.clear_content()
    p_lh = left.add_paragraph()
    p_lh.paragraph_format.space_before = Pt(2)
    p_lh.paragraph_format.space_after = Pt(2)
    run = p_lh.add_run('关键数据  /  KEY METRICS')
    run.font.size = Pt(9)
    run.font.bold = True
    run.font.color.rgb = COLOR_PRIMARY
    for big, desc in params['metrics']:
        p_m = left.add_paragraph()
        p_m.paragraph_format.space_before = Pt(0)
        p_m.paragraph_format.space_after = Pt(1)
        run = p_m.add_run(f'  {big}  ')
        run.font.size = Pt(13)
        run.font.bold = True
        run.font.color.rgb = COLOR_PRIMARY
        run = p_m.add_run(desc)
        run.font.size = Pt(8.5)
        run.font.color.rgb = COLOR_TERTIARY

    # 右列：元信息
    right = table1.rows[0].cells[1]
    right._tc.clear_content()
    p_rh = right.add_paragraph()
    p_rh.paragraph_format.space_before = Pt(2)
    p_rh.paragraph_format.space_after = Pt(2)
    run = p_rh.add_run('报告元信息  /  META')
    run.font.size = Pt(9)
    run.font.bold = True
    run.font.color.rgb = COLOR_PRIMARY
    for label, value in params['metas']:
        p_meta = right.add_paragraph()
        p_meta.paragraph_format.space_before = Pt(0)
        p_meta.paragraph_format.space_after = Pt(1)
        run = p_meta.add_run(f'  {label}  ')
        run.font.size = Pt(8.5)
        run.font.color.rgb = COLOR_TERTIARY
        run = p_meta.add_run(value)
        run.font.size = Pt(9.5)
        run.font.bold = True
        run.font.color.rgb = COLOR_BODY

    # 5. Banner 图（缩小版 居中）
    p_banner = doc.add_paragraph()
    p_banner.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p_banner.paragraph_format.space_before = Pt(6)
    p_banner.paragraph_format.space_after = Pt(4)
    banner_path = render_banner('/tmp/deep-research-banner-cache/banner.png')
    run_banner = p_banner.add_run()
    run_banner.add_picture(banner_path, width=Cm(16))

    # 6. 底部 brand 浅灰条
    p_brand_bot = doc.add_paragraph()
    p_brand_bot.paragraph_format.space_before = Pt(4)
    p_brand_bot.paragraph_format.space_after = Pt(0)
    pPr = p_brand_bot._p.get_or_add_pPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:fill'), 'F2F2F2')
    pPr.append(shd)
    p_brand_bot.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p_brand_bot.add_run(params.get('footer', '本报告共 9 章正文 + 4 附录  ·  麦肯锡风 v5.2  ·  决策导向'))
    run.font.size = Pt(8)
    run.font.color.rgb = COLOR_TERTIARY



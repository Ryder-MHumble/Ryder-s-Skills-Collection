def add_hyperlink(paragraph, text, url, size=9.5, color='0563C1'):
    """添加可点击超链接"""
    part = paragraph.part
    r_id = part.relate_to(
        url,
        'http://schemas.openxmlformats.org/officeDocument/2006/relationships/hyperlink',
        is_external=True
    )
    hyperlink = OxmlElement('w:hyperlink')
    hyperlink.set(qn('r:id'), r_id)
    new_run = OxmlElement('w:r')
    rPr = OxmlElement('w:rPr')
    c = OxmlElement('w:color'); c.set(qn('w:val'), color)
    rPr.append(c)
    u = OxmlElement('w:u'); u.set(qn('w:val'), 'single')
    rPr.append(u)
    sz = OxmlElement('w:sz'); sz.set(qn('w:val'), str(int(size * 2)))
    rPr.append(sz)
    new_run.append(rPr)
    new_run.text = text
    hyperlink.append(new_run)
    paragraph._p.append(hyperlink)
    return paragraph


def set_cell_shading(cell, color_hex):
    shading_elm = OxmlElement('w:shd')
    shading_elm.set(qn('w:fill'), color_hex)
    shading_elm.set(qn('w:val'), 'clear')
    cell._tc.get_or_add_tcPr().append(shading_elm)


def format_table_mckinsey(table):
    """麦肯锡风格表格：白底 + 蓝头条 + 灰交替行 + 细灰边框"""
    table.alignment = WD_TABLE_ALIGNMENT.LEFT  # 麦肯锡表格左对齐
    # 表头：深蓝底 + 白字 + 加粗
    for cell in table.rows[0].cells:
        set_cell_shading(cell, COLOR_TABLE_HEADER)
        for p in cell.paragraphs:
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            p.paragraph_format.space_before = Pt(2)
            p.paragraph_format.space_after = Pt(2)
            for r in p.runs:
                r.font.color.rgb = COLOR_WHITE
                r.font.bold = True
                r.font.size = Pt(10.5)
    # 数据行：白底/灰底交替 + 黑色字
    for i, row in enumerate(table.rows[1:], 1):
        for cell in row.cells:
            if i % 2 == 0:
                set_cell_shading(cell, COLOR_TABLE_ALT)
            for p in cell.paragraphs:
                p.paragraph_format.space_before = Pt(2)
                p.paragraph_format.space_after = Pt(2)
                for r in p.runs:
                    r.font.size = Pt(10)
                    r.font.color.rgb = COLOR_BODY


def make_kv_table(doc, data_rows, headers=None):
    """2 列 KV 表格"""
    if headers is None:
        headers = ['维度', '详情']
    t = doc.add_table(rows=1 + len(data_rows), cols=2)
    t.style = 'Table Grid'
    t.rows[0].cells[0].text = headers[0]
    t.rows[0].cells[1].text = headers[1]
    for i, (k, v) in enumerate(data_rows):
        t.rows[i+1].cells[0].text = str(k)
        t.rows[i+1].cells[1].text = str(v)
    format_table_mckinsey(t)
    # 加粗左列 - 麦肯锡风格
    for row in t.rows[1:]:
        for p in row.cells[0].paragraphs:
            for r in p.runs:
                r.font.bold = True
                r.font.color.rgb = COLOR_PRIMARY
    return t


def make_data_table(doc, headers, data_rows):
    """多列数据表格"""
    t = doc.add_table(rows=1 + len(data_rows), cols=len(headers))
    t.style = 'Table Grid'
    for j, h in enumerate(headers):
        t.rows[0].cells[j].text = h
    for i, row in enumerate(data_rows):
        for j, val in enumerate(row):
            t.rows[i+1].cells[j].text = str(val)
    format_table_mckinsey(t)
    return t


